void solve() {
    int n, m;
    cin >> n >> m;
    vector<vector<int>>adj(n);
    for (int i = 0; i < m; ++i) {
        int x, y;
        cin >> x >> y;
        --x; --y;
        adj[x].push_back(y);
        adj[y].push_back(x);
    }
    for (int i = 0; i < n; ++i) {
        if (adj[i].size() == 1) {
            int neigh = adj[i][0];
            for (int j = 0; j < (int) adj[neigh].size(); ++j) {
                int super_neigh = adj[neigh][j];
                if (adj[super_neigh].size() > 1) {
                    cout << adj[super_neigh].size() << " " << adj[neigh].size()-1 << endl;
                    return;
                }
            }
        }
    }
    return;
}

int main() {
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    cout.tie(NULL);

    int t; cin >> t;
    while (t--) {
        solve();
    }

    return 0;
}
